
public class LogicalOps {
	public static void main(String args[]) {
		 boolean x=true, y=false;
		 System.out.println(x&&y);
		 System.out.println(x||y);
		 System.out.println(!x);
		 
		 
		 
		 int a=10,b=5;
		 System.out.println(a==b);
		 System.out.println(a!=b);
		 System.out.println(a<b);
		 
		 //bitwise
		 System.out.println(a&b);
		 System.out.println(a^b);
		 
	}

}
